FACTOR_QUESTIONS = {}
for num, content, factor in SCL90_QUESTIONS:
    if factor not in FACTOR_QUESTIONS:
        FACTOR_QUESTIONS[factor] = []
    FACTOR_QUESTIONS[factor].append(num)

FACTOR_COUNTS = {k: len(v) for k, v in FACTOR_QUESTIONS.items()}


def calculate_scores(answers):
    scores = {}
    for factor, nums in FACTOR_QUESTIONS.items():
        total = sum(answers.get(num, 0) for num in nums)
        count = FACTOR_COUNTS[factor]
        scores[factor] = round(total / count, 2) if count > 0 else 0.0

    total_sum = sum(answers.values())
    gsi = round(total_sum / 90, 2)

    positive_count = sum(1 for v in answers.values() if v > 1)
    positive_sum = sum(v for v in answers.values() if v > 1)
    positive_avg = round(positive_sum / positive_count, 2) if positive_count > 0 else 0.0

    return {
        'factor_scores': scores,
        'gsi': gsi,
        'positive_count': positive_count,
        'positive_avg': positive_avg,
        'total_sum': total_sum
    }


def get_risk_level(factor_score, norm_mean, norm_std):
    if factor_score <= norm_mean + norm_std:
        return 'green'
    elif factor_score <= norm_mean + 2 * norm_std:
        return 'yellow'
    else:
        return 'red'


def compare_with_norm(factor_scores):
    result = {}
    for factor_code, factor_name, mean, std, desc in NORM_DATA:
        if factor_code in factor_scores:
            score = factor_scores[factor_code]
            level = get_risk_level(score, mean, std)
            if score > mean + std:
                status = '偏高'
            elif score < mean - std:
                status = '偏低'
            else:
                status = '正常'
            result[factor_code] = {
                'name': factor_name,
                'score': score,
                'norm_mean': mean,
                'norm_std': std,
                'status': status,
                'risk_level': level,
                'description': desc
            }
    return result
